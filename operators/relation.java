package operators;

public class relation {
    public static void main(String args[]){
        //==
        //!=
        //>
        //<  these all are relation  operator
        //>=
        //<=
        int a=10;
        int b=20;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);


    }
}
